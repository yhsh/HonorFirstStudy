public class LiveDataDemo extends ViewModel {
    public MutableLiveData<Integer> number;

    public MutableLiveData<Integer> getNumber() {
        if (number == null) {
            number = new MutableLiveData<Integer>();
            number.setValue(0);
        }
        return number;
    }

    public void addLikeAndDisLike(int num) {
        Integer value = number.getValue();
        value += num;
        number.setValue(value);
    }
}
